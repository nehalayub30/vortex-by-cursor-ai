    /**
     * Save custom fields from the term form.
     *
     * @since    1.0.0
     * @param    int    $term_id    The term ID.
     */
    public function save_term_fields( $term_id ) {
        // Save color
        if ( isset( $_POST['vortex_tag_color'] ) ) {
            update_term_meta( 
                $term_id, 
                'vortex_tag_color', 
                sanitize_hex_color( $_POST['vortex_tag_color'] ) 
            );
        }
        
        // Save trending status
        $trending = isset( $_POST['vortex_tag_trending'] ) ? 1 : 0;
        update_term_meta( $term_id, 'vortex_tag_trending', $trending );
        
        // Save weight
        if ( isset( $_POST['vortex_tag_weight'] ) ) {
            update_term_meta( 
                $term_id, 
                'vortex_tag_weight', 
                intval( $_POST['vortex_tag_weight'] ) 
            );
        }
        
        // Save related tags
        if ( isset( $_POST['vortex_tag_related'] ) ) {
            update_term_meta( 
                $term_id, 
                'vortex_tag_related', 
                sanitize_text_field( $_POST['vortex_tag_related'] ) 
            );
        }
    }

    /**
     * Modify the columns in the term list table.
     *
     * @since    1.0.0
     * @param    array    $columns    The default columns.
     * @return   array                Modified columns.
     */
    public function modify_term_columns( $columns ) {
        $new_columns = array();
        
        // Add columns after name column
        foreach ( $columns as $key => $value ) {
            $new_columns[$key] = $value;
            
            if ( $key === 'name' ) {
                $new_columns['color'] = __( 'Color', 'vortex-ai-marketplace' );
                $new_columns['trending'] = __( 'Trending', 'vortex-ai-marketplace' );
                $new_columns['weight'] = __( 'Weight', 'vortex-ai-marketplace' );
            }
        }
        
        return $new_columns;
    }

    /**
     * Populate the custom columns in the term list table.
     *
     * @since    1.0.0
     * @param    string    $content      The column content.
     * @param    string    $column_name  The column name.
     * @param    int       $term_id      The term ID.
     * @return   string                  The column content.
     */
    public function populate_term_columns( $content, $column_name, $term_id ) {
        switch ( $column_name ) {
            case 'color':
                $color = get_term_meta( $term_id, 'vortex_tag_color', true );
                if ( ! empty( $color ) ) {
                    $content = '<span style="display:inline-block; width:20px; height:20px; background-color:' . esc_attr( $color ) . '; vertical-align:middle; border-radius:50%;"></span> ' . $color;
                } else {
                    $content = '—';
                }
                break;
                
            case 'trending':
                $trending = get_term_meta( $term_id, 'vortex_tag_trending', true );
                $content = $trending ? '<span class="dashicons dashicons-star-filled" style="color:gold;"></span>' : '—';
                break;
                
            case 'weight':
                $weight = get_term_meta( $term_id, 'vortex_tag_weight', true );
                $content = ! empty( $weight ) ? $weight : '0';
                break;
        }
        
        return $content;
    }

    /**
     * Register REST API fields for tag taxonomy.
     *
     * @since    1.0.0
     */
    public function register_rest_fields() {
        register_rest_field(
            $this->taxonomy,
            'tag_meta',
            array(
                'get_callback'    => array( $this, 'get_term_meta_for_api' ),
                'update_callback' => null,
                'schema'          => null,
            )
        );
    }

    /**
     * Get term meta data for REST API.
     *
     * @since    1.0.0
     * @param    array     $object      The term object.
     * @param    string    $field_name  The field name.
     * @param    array     $request     The request data.
     * @return   array                  The term meta data.
     */
    public function get_term_meta_for_api( $object, $field_name, $request ) {
        $term_id = $object['id'];
        
        return array(
            'color'    => get_term_meta( $term_id, 'vortex_tag_color', true ),
            'trending' => (bool) get_term_meta( $term_id, 'vortex_tag_trending', true ),
            'weight'   => (int) get_term_meta( $term_id, 'vortex_tag_weight', true ),
            'related'  => get_term_meta( $term_id, 'vortex_tag_related', true ),
            'count'    => (int) $object['count'],
        );
    }

